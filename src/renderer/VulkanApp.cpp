VkResult VulkanApp::CreateDebugUtilsMessengerEXT(
    VkInstance instanceValue,
    const VkDebugUtilsMessengerCreateInfoEXT* pCreateInfo,
    const VkAllocationCallbacks* pAllocator,
    VkDebugUtilsMessengerEXT* pDebugMessenger
) {
    auto func = reinterpret_cast<PFN_vkCreateDebugUtilsMessengerEXT>(
        vkGetInstanceProcAddr(instanceValue, "vkCreateDebugUtilsMessengerEXT")
    );
    if (func != nullptr) {
        return func(instanceValue, pCreateInfo, pAllocator, pDebugMessenger);
    }
    return VK_ERROR_EXTENSION_NOT_PRESENT;
}

void VulkanApp::DestroyDebugUtilsMessengerEXT(
    VkInstance instanceValue,
    VkDebugUtilsMessengerEXT messenger,
    const VkAllocationCallbacks* pAllocator
) {
    auto func = reinterpret_cast<PFN_vkDestroyDebugUtilsMessengerEXT>(
        vkGetInstanceProcAddr(instanceValue, "vkDestroyDebugUtilsMessengerEXT")
    );
    if (func != nullptr) {
        func(instanceValue, messenger, pAllocator);
    }
}
